#include "libft.h"

int ft_atoi(const char *str)
{
	int i;
	int res;
	int sign;

	sign = 1;
	i = 0;
	res = 0;
	while (str[i] <= 32)
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (sign * res);
}
